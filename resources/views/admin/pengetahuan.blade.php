<b><ol>
<li><strong>Depresi</strong></li>
</ol></b>
<ul>
<li style="text-align: justify;">Depresi adalah gangguan suasana hati yang ditandai oleh perasaan sedih, kehilangan minat atau kegembiraan dalam aktivitas sehari-hari, perubahan nafsu makan atau tidur, kelelahan, dan perasaan tidak berharga.</li>
<li style="text-align: justify;">Saran awal pengobatan : Konseling terapi bicara (psikoterapi), penggunaan obat-obatan antidepresan, dukungan keluarga dan teman-teman, perubahan gaya hidup yang sehat seperti olahraga teratur dan pola tidur yang baik.</li>
</ul></div>
<hr>
<img height="150" src="{{ asset('dist/img/skizo.png') }}" style="float: right; margin: 0px 0px 3px 9px;" width="150" />
<b><ol start="2">
<li><strong>Skizofrenia</strong></li>
</ol></b>
<ul>
<li style="text-align: justify;">Skizofrenia adalah gangguan mental serius yang mempengaruhi cara seseorang berpikir, merasa, dan berperilaku. Gejala dapat mencakup halusinasi, delusi, gangguan berpikir, dan kesulitan dalam memahami realitas.</li>
<li style="text-align: justify;">Saran awal pengobatan: Terapi antipsikotik untuk mengontrol gejala psikotik, terapi konseling untuk membantu dalam mengelola kondisi dan meningkatkan fungsi sosial.</li>
</ul>
<hr>
<img height="150" src="{{ asset('dist/img/ptsd.png') }}" style="float: right; margin: 0px 0px 3px 9px;" width="150" />
<b><ol start="3">
<li><strong>Post Traumatic Stress Disorder (PTSD</strong>)</li>
</ol></b>
<ul>
<li style="text-align: justify;">PTSD adalah gangguan mental yang berkembang setelah mengalami atau menyaksikan peristiwa traumatis. Gejalanya dapat mencakup mimpi buruk, kilas balik, kecemasan, dan perilaku menghindar.</li>
<li style="text-align: justify;">Saran awal pengobatan: Terapi trauma seperti terapi kognitif perilaku (CBT) atau terapi mata bergerak cepat (EMDR), penggunaan obat-obatan seperti antidepresan atau anti-kecemasan untuk mengurangi gejala.</li>
</ul>
<hr>
<img height="150" src="{{ asset('dist/img/paranoid.png') }}" style="float: right; margin: 0px 0px 3px 9px;" width="150" />
<b><ol start="4">
<li><strong>Paranoid</strong></li>
</ol></b>
<ul>
<li style="text-align: justify;">Paranoid adalah kondisi di mana seseorang memiliki kecurigaan yang berlebihan terhadap orang lain dan lingkungannya, bahkan tanpa bukti konkret.</li>
<li style="text-align: justify;">Saran awal pengobatan: Terapi kognitif perilaku (CBT) atau terapi bicara untuk membantu mengidentifikasi dan mengubah pola pikir yang negatif, penggunaan obat-obatan seperti antipsikotik jika gejala berat.</li>
</ul>
<hr>
<img height="150" src="{{ asset('dist/img/bipolar.png') }}" style="float: right; margin: 0px 0px 3px 9px;" width="150" />
<b><ol start="5">
<li><strong>Bipolar</strong></li>
</ol></b>
<ul>
<li style="text-align: justify;">Bipolar adalah gangguan suasana hati yang ditandai oleh perubahan antara episode mania (periode tingkat energi yang tinggi) dan depresi (periode sedih atau rendah).</li>
<li style="text-align: justify;">Saran awal pengobatan: Penggunaan stabilisator suasana hati atau obat antipsikotik untuk mengatur suasana hati, terapi konseling untuk membantu individu dalam mengelola gejala dan memahami kondisi mereka.</li>
</ul>
<hr>
<img height="150" src="{{ asset('dist/img/ocd.png') }}" style="float: right; margin: 0px 0px 3px 9px;" width="150" />
<b><ol start="6">
<li><strong>Obsessive Compulsive Disorder (OCD)</strong></li>
</ol></b>
<ul>
<li style="text-align: justify;">OCD adalah gangguan kecemasan yang ditandai oleh pikiran obsesif yang tidak diinginkan dan perilaku kompulsif untuk meredakan kecemasan tersebut.</li>
<li style="text-align: justify;">Saran awal pengobatan: Terapi kognitif perilaku (CBT) atau terapi eksposur dan respons-preventif (ERP) untuk mengubah pola pikir dan perilaku, penggunaan obat-obatan seperti antidepresan atau anti-kecemasan.</li>
</ul>
<hr>
<img height="150" src="{{ asset('dist/img/anxiety.png') }}" style="float: right; margin: 0px 0px 3px 9px;" width="150" />
<b><ol start="7">
<li><strong>Anxiety Disorder</strong></li>
</ol></b>
<ul>
<li style="text-align: justify;">Anxiety Disorder adalah kelompok kondisi mental yang mencakup kecemasan yang berlebihan, ketakutan, atau ketegangan yang dapat mengganggu kehidupan sehari-hari.</li>
<li style="text-align: justify;">Saran awal pengobatan: Terapi kognitif perilaku (CBT) atau terapi bicara untuk mengidentifikasi dan mengubah pola pikir yang negatif, relaksasi dan teknik pernapasan untuk mengelola kecemasan, penggunaan obat-obatan seperti antidepresan atau anti-kecemasan jika diperlukan.</li>
</ul>
